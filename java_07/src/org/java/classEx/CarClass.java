package org.java.classEx;

public class CarClass {
	
	public String carName;
	public String carYear;
	public int carCoin;
	public String carColor;
	
	public void carMethod() {
		System.out.println("이름 : "+this.carName);
		System.out.println("연식 : "+this.carYear);
		System.out.println("가격 : "+this.carCoin);
		System.out.println("색상 : "+this.carColor);
	}

}
